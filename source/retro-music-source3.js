// 功能：把 MIDI 音高转换成频率；入参为 MIDI 数字；返回赫兹值。
function midiToFrequency(midiNumber) {
  if (midiNumber < 0) {
    return 0;
  } else {
    return 440 * math_pow(2, (midiNumber - 69) / 12);
  }
}

// 功能：统计轨道事件数量；入参为事件数组；返回事件数。
function eventCount(events) {
  return array_length(events);
}

// 功能：合并两个事件数组；入参为两条事件轨；返回新的事件数组。
function mergeEvents(firstEvents, secondEvents) {
  const merged = [];
  let index = 0;
  for (index = 0; index < array_length(firstEvents); index = index + 1) {
    merged[index] = firstEvents[index];
  }
  for (index = 0; index < array_length(secondEvents); index = index + 1) {
    merged[array_length(firstEvents) + index] = secondEvents[index];
  }
  return merged;
}

// 功能：把原曲所有轨道合并；无入参；返回完整事件数组。
function buildFullSong() {
  const melody = mergeEvents(INTRO_SPARK_EVENTS, MAIN_MELODY_EVENTS);
  const harmony = mergeEvents(melody, HARMONY_EVENTS);
  const bass = mergeEvents(harmony, BASS_EVENTS);
  const openingDrums = mergeEvents(bass, OPENING_DRUM_EVENTS);
  return mergeEvents(openingDrums, PERCUSSION_EVENTS);
}

